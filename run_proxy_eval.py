def cmd_run_skill_script(args):
    skill = SKILLS.get(args.skill)
    if skill is None:
        print(json.dumps({{"error": f"Skill '{{args.skill}}' not found.", "error_code": "SKILL_NOT_FOUND"}}))
        return
    script_name = args.script
    src = skill["scripts"].get(script_name)
    if src is None:
        print(json.dumps({{"error": f"Script '{{script_name}}' not found in skill '{{args.skill}}'.", "error_code": "RESOURCE_NOT_FOUND"}}))
        return
    script_args = {{}}
    if args.args:
        try:
            script_args = json.loads(args.args)
        except json.JSONDecodeError as e:
            print(json.dumps({{"error": f"--args must be a JSON object: {{e}}", "error_code": "INVALID_ARGUMENTS"}}))
            return
    orig_cwd = os.getcwd()
    with tempfile.TemporaryDirectory() as td:
        for kind in ("references", "assets", "scripts"):
            for name, content in skill[kind].items():
                full = os.path.join(td, kind, name)
                os.makedirs(os.path.dirname(full), exist_ok=True)
                mode = "wb" if isinstance(content, bytes) else "w"
                with open(full, mode) as f:
                    f.write(content)
        script_path = os.path.join(td, "scripts", script_name)
        argv = [script_path]
        for k, v in script_args.items():
            argv.extend([f"--{{k}}", str(v)])
        os.chdir(td)
        try:
            old_argv = sys.argv
            sys.argv = argv
            try:
                runpy.run_path(script_path, run_name="__main__")
                print(json.dumps({{"status": "ok", "note": "script completed; check its own stdout above for output paths"}}))
            except SystemExit as e:
                if e.code not in (None, 0):
                    print(json.dumps({{"error": f"Script exited with code {{e.code}}", "error_code": "EXECUTION_ERROR"}}))
            finally:
                sys.argv = old_argv
        except Exception as e:  # noqa: BLE001
            print(json.dumps({{"error": f"{{type(e).__name__}}: {{e}}", "error_code": "EXECUTION_ERROR"}}))
        finally:
            os.chdir(orig_cwd)


