#!/usr/bin/env python3
"""Local evaluation harness that uses a Claude subagent as a stand-in LLM.

`run_local_eval.py` drives a submission's agent.yaml through the real Google ADK
harness, calling the configured Gemini model. That costs real API quota/tokens
and needs the podman-hosted Kaggle sandbox image.

This script is a *diagnostic*, not a replacement: it drives the same agent.yaml
(system prompt + skills) through a headless `claude -p` process instead, so we
can cheaply and repeatedly observe how an LLM reasons about and uses this
project's tool/skill interface -- catching prompt ambiguity, workflow-ordering
issues, and skill-usage mistakes -- without spending Gemini budget.

**What this is NOT**: it does not tell us anything about how the actual
configured Gemini model (e.g. gemini-3.5-flash) will behave. Claude is
generally much more careful about following multi-step instructions, so a
clean run here is not proof the real harness run will also be clean. Use
`run_local_eval.py` (spends real quota) before trusting any of this on a
graded run.

**Known risk**: the proxy session runs with `--dangerously-skip-permissions`
(headless mode can't answer interactive prompts) and an unrestricted Bash
tool, so it has the same filesystem access as your own user account --
including, in principle, `data/<dataset>/solution.csv`, which sits on the same
machine (unlike the real sandbox, where it never exists in-container at all).
The dispatcher script never prints or discusses that path, and nothing in the
prompt suggests looking for it, but this is not a hard sandbox boundary --
don't run this against anything you wouldn't want a cooperative-but-unsandboxed
agent to be able to read or write.

## Usage
    python run_proxy_eval.py --submission-dir submissions/07_encoding_plus_ensemble/agent \
        --dataset train_01 --model claude-haiku-4-5
"""

