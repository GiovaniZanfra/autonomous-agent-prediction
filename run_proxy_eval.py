def _score_csv(pred_path, usage_filter):
    id_col, solution = _load_solution()
    pairs = []
    with open(pred_path, newline="", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        pred_id_col = reader.fieldnames[0]
        pred_col = [c for c in reader.fieldnames if c != pred_id_col][0]
