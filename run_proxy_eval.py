def cmd_agent_tool_result(args):
    if not os.path.exists(args.output_file):
        print(json.dumps({{"error": "output_file not found", "error_code": "FILE_NOT_FOUND"}}))
        return
    with open(args.output_file) as f:
        content = f.read().strip()
    if not content:
        print(json.dumps({{"status": "still_running", "note": "No output yet, check again shortly."}}))
        return
    try:
        result = json.loads(content)
        print(json.dumps({{"status": "done", "result": result.get("result", content)}}))
    except json.JSONDecodeError:
        print(json.dumps({{"status": "still_running", "note": "Output not complete yet, check again shortly."}}))


