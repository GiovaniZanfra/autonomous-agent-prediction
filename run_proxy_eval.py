def discover_skill(skill_dir: Path) -> dict:
    """Read a skill directory's SKILL.md and its references/assets/scripts contents."""
    skill_md = (skill_dir / "SKILL.md").read_text(encoding="utf-8")

    def read_dir(name):
        d = skill_dir / name
        out = {}
        if d.is_dir():
            for f in sorted(d.iterdir()):
                if f.is_file():
                    try:
                        out[f.name] = f.read_text(encoding="utf-8")
                    except UnicodeDecodeError:
                        out[f.name] = f.read_bytes()
        return out

    return {
        "name": skill_dir.name,
        "skill_md": skill_md,
        "references": read_dir("references"),
        "assets": read_dir("assets"),
        "scripts": read_dir("scripts"),
    }


def collect_subagents(agent_cfg: dict, agent_dir: Path) -> dict:
    """Find agent_tool entries in the top-level tools list and resolve their configs."""
    subagents = {}
    for tool_entry in agent_cfg.get("tools", []):
        if isinstance(tool_entry, dict) and "agent_tool" in tool_entry:
            config_path = tool_entry["agent_tool"]["config_path"]
            sub_cfg = load_yaml(agent_dir / config_path, root_dir=agent_dir)
            subagents[sub_cfg["name"]] = {
                "instruction": sub_cfg["instruction"],
                "tools": [t for t in sub_cfg.get("tools", []) if isinstance(t, str)],
                "description": sub_cfg.get("description", ""),
            }
    return subagents


