def _auc(pairs):
    """pairs: list of (label, score). Rank-based AUC, stdlib only, tie-averaged."""
    pairs = sorted(pairs, key=lambda p: p[1])
    n = len(pairs)
    ranks = [0.0] * n
    i = 0
    while i < n:
        j = i
        while j + 1 < n and pairs[j + 1][1] == pairs[i][1]:
            j += 1
        avg_rank = (i + j) / 2.0 + 1.0
        for k in range(i, j + 1):
            ranks[k] = avg_rank
        i = j + 1
    sum_ranks_pos = sum(r for (lbl, _), r in zip(pairs, ranks) if lbl == 1)
    n_pos = sum(1 for lbl, _ in pairs if lbl == 1)
    n_neg = n - n_pos
    if n_pos == 0 or n_neg == 0:
        return None
    return (sum_ranks_pos - n_pos * (n_pos + 1) / 2.0) / (n_pos * n_neg)


