def cmd_edit_file(args):
    payload = json.loads(sys.stdin.read())
    path = payload["path"]
    full_path = os.path.join(SANDBOX_DIR, path) if not os.path.isabs(path) else path
    if not os.path.exists(full_path):
        print(json.dumps({{"error": f"File not found: {{path}}", "error_code": "FILE_NOT_FOUND"}}))
        return
    with open(full_path, encoding="utf-8") as f:
        text = f.read()
    old = payload["old_string"]
    new = payload["new_string"]
    replace_all = payload.get("replace_all", False)
    count = text.count(old)
    if count == 0:
        print(json.dumps({{"error": "old_string not found in file", "error_code": "NOT_FOUND"}}))
        return
    if count > 1 and not replace_all:
        print(json.dumps({{
            "error": f"old_string is not unique ({{count}} occurrences); pass replace_all=true or make old_string more specific",
            "error_code": "NOT_UNIQUE",
        }}))
        return
    new_text = text.replace(old, new) if replace_all else text.replace(old, new, 1)
    with open(full_path, "w", encoding="utf-8") as f:
        f.write(new_text)
    print(json.dumps({{"status": "ok", "path": full_path, "replacements": count if replace_all else 1}}))


